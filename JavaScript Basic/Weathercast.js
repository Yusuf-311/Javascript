let kelvin = 0; // Kelvin value is 45

let celcius = kelvin - 273.15; // Celsius value is Kelvin - 273.15

let fahrenheit = (celcius * (9/5)) + 32; // Fahrenheit value

fahrenheit = Math.floor(fahrenheit); // Rounding down Fahrenheit to the nearest whole number

console.log(`The temperature is ${fahrenheit} degrees Fahrenheit`); // Output: The temperature is <fahrenheit> degrees Fahrenheit
